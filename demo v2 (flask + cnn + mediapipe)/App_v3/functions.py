def draw_info_text(image, brect, handedness, hand_sign_text, confidence_score):
    cv.rectangle(image, (brect[0], brect[1]), (brect[2], brect[1] - 22), (0, 0, 0), -1)
    info_text = handedness.classification[0].label[0:]
    if hand_sign_text != "":
        info_text = f"{info_text}: {hand_sign_text} ({confidence_score:.2f}%)"
    cv.putText(image, info_text, (brect[0] + 5, brect[1] - 4), cv.FONT_HERSHEY_SIMPLEX, 0.6, (255, 255, 255), 1, cv.LINE_AA)
    return image

def keybind(label, last_keybind_time):
    if last_keybind_time is None:
        last_keybind_time = time.time()  # Initialize if it's None

    current_time = time.time()  # Get current time
    if current_time - last_keybind_time < 3:  # Delay 3 seconds
        return last_keybind_time

    # The keybind activation logic
    if label == '0':
        print('Play/Pause')
        pyautogui.hotkey('space')

    elif label == '1':
        print('Next Track')
        pyautogui.hotkey('ctrl', 'right')

    elif label == '2':
        print('Previous Track')
        pyautogui.hotkey('ctrl', 'left')

    elif label == '3':
        print('Volume Up')
        pyautogui.hotkey('ctrl', 'up')

    elif label == '4':
        print('Volume Down')
        pyautogui.hotkey('ctrl', 'down')
        
    elif label == '5':
        print('Repeat Track')
        pyautogui.hotkey('ctrl', 'r')

    elif label == 'Unknown':
        print('Unknown Keybind')

    # Update the last keybind time after performing the action
    last_keybind_time = current_time

    return last_keybind_time  # Return updated time

